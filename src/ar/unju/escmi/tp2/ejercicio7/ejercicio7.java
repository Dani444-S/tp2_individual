package ar.unju.escmi.tp2.ejercicio7;

public class ejercicio7 {
     public static void main(String[] args) {
        
        int a = 30;
        double b = 1.5;

        System.out.println(a++); // Muestra el valor de 'a' para luego incrementar.

        System.out.println(a); // Muestra 'a' con su valor actual.

        System.out.println(--a); // Decrementa el valor de 'a' para luego mostrar.

        System.out.println(++b); // Incrementa el valor de 'b' para luego mostrar

        System.out.println(b--); // Muestra el valor de 'b' y luego lo decrementa.

        System.out.println(b); // Muestra 'b' con su valor actual.
    }
}
